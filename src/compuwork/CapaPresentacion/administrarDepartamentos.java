    private void llenarTablaDepartamentos() {
        javax.swing.table.DefaultTableModel modelo = (javax.swing.table.DefaultTableModel) tblDepartamentos.getModel();
        modelo.setRowCount(0); // Limpiar la tabla antes de llenarla

        for (Departamento depto : Empresa.Departamentos) {
            Object[] fila = {
                depto.getCodigo(),
                depto.getNombre()
            };
            modelo.addRow(fila);
        }
    }

    public void actualizarTabla() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Código");
        modelo.addColumn("Nombre");

        for (Departamento d : empresa.getDepartamentos()) {
            Object[] fila = {d.getCodigo(), d.getNombre()};
            modelo.addRow(fila);
        }

        tblDepartamentos.setModel(modelo);
    }

