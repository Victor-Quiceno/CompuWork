    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:

        int filaSeleccionada = tblDepartamentos.getSelectedRow();

        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(this, "Selecciona un departamento para editar.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Obtener el código del departamento desde la tabla (columna 0)
        int codigo = Integer.parseInt(tblDepartamentos.getValueAt(filaSeleccionada, 0).toString());

        // Buscar el departamento en la lista de la empresa
        Departamento departamentoSeleccionado = null;
        for (Departamento d : empresa.getDepartamentos()) {
            if (d.getCodigo() == codigo) {
                departamentoSeleccionado = d;
                break;
            }
        }

        if (departamentoSeleccionado != null) {
            EditarDepartamentoDialog dialog = new EditarDepartamentoDialog(administrarDepartamentos.this, true, departamentoSeleccionado);
            dialog.setVisible(true);
            actualizarTabla(); // Refrescar la tabla después de editar
        }


    }//GEN-LAST:event_btnEditarActionPerformed

