    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int filaSeleccionada = tblDepartamentos.getSelectedRow();

        if (filaSeleccionada != -1) {
            int confirmacion = JOptionPane.showConfirmDialog(
                    this,
                    "¿Estás seguro de que deseas eliminar este departamento?",
                    "Confirmar eliminación",
                    JOptionPane.YES_NO_OPTION
            );

            if (confirmacion == JOptionPane.YES_OPTION) {
                // Obtener el ID del departamento seleccionado desde la tabla
                int idDepartamento = (int) tblDepartamentos.getValueAt(filaSeleccionada, 0);

                // Buscar y eliminar de la lista
                Departamento departamentoAEliminar = null;
                for (Departamento d : empresa.getDepartamentos()) {
                    if (d.getCodigo() == idDepartamento) {
                        departamentoAEliminar = d;
                        break;
                    }
                }

                if (departamentoAEliminar != null) {
                    empresa.getDepartamentos().remove(departamentoAEliminar);
                    actualizarTabla(); // Refrescar la tabla
                    JOptionPane.showMessageDialog(this, "Departamento eliminado con éxito.");
                } else {
                    JOptionPane.showMessageDialog(this, "No se pudo encontrar el departamento a eliminar.");
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Por favor selecciona un departamento para eliminar.");
        }


    }//GEN-LAST:event_btnEliminarActionPerformed

