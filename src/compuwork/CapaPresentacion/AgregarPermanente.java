    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed

        try {

            if (empleadoAEditar == null) {
                btnAgregar.setText("Agregar");
            } else {
                btnAgregar.setText("Guardar");
            }

            // Obtener y validar datos
            int id = Integer.parseInt(txtId.getText().trim());
            String nombre = txtNombre.getText().trim();
            String correo = txtCorreo.getText().trim();
            double sueldo = Double.parseDouble(txtSueldo.getText().trim());
            String fechaIngreso = txtFechaIngreso.getText().trim();
            String cargo = txtCargo.getText().trim();

            if (nombre.isEmpty() || correo.isEmpty() || fechaIngreso.isEmpty() || cargo.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Todos los campos deben estar llenos.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Validar formato de la fecha
            if (!fechaIngreso.matches("\\d{2}/\\d{2}/\\d{4}")) {
                JOptionPane.showMessageDialog(this, "La fecha debe tener el formato dd/MM/yyyy.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Obtener el departamento seleccionado
            String nombreDepto = (String) comboDepartamentos.getSelectedItem();
            Departamento depSeleccionado = empresa.buscarDepartamentoPorNombre(nombreDepto);

            if (depSeleccionado == null) {
                JOptionPane.showMessageDialog(this, "Debe seleccionar un departamento.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            if (empleadoAEditar == null) {
                // MODO AGREGAR
                Permanente nuevoPermanente = new Permanente(sueldo, fechaIngreso, cargo, id, nombre, correo);
                depSeleccionado.agregarEmpleado(nuevoPermanente);
                JOptionPane.showMessageDialog(this, "Empleado agregado exitosamente.");
            } else {
                // MODO EDITAR

                // Ver si el departamento cambió
                Departamento departamentoActual = empleadoAEditar.getDepartamento();
                if (!departamentoActual.equals(depSeleccionado)) {
                    // Mover el empleado al nuevo departamento
                    departamentoActual.eliminarEmpleado(empleadoAEditar);
                    depSeleccionado.agregarEmpleado(empleadoAEditar);
                }

                // Actualizar datos del empleado
                empleadoAEditar.setNombre(nombre);
                empleadoAEditar.setCorreoElectronico(correo);
                empleadoAEditar.setSueldo(sueldo);
                empleadoAEditar.setFechaIngreso(fechaIngreso);
                empleadoAEditar.setCargo(cargo);

                JOptionPane.showMessageDialog(this, "Empleado actualizado exitosamente.");
            }

            dispose();

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "ID y sueldo deben ser valores numéricos.", "Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Ocurrió un error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_btnAgregarActionPerformed

