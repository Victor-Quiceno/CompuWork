    private void cargarDatosEnCampos() {
        if (empleadoAEditar != null) {
            txtId.setText(String.valueOf(empleadoAEditar.getId()));
            txtNombre.setText(empleadoAEditar.getNombre());
            txtCorreo.setText(empleadoAEditar.getCorreoElectronico());
            txtSueldo.setText(String.valueOf(empleadoAEditar.getSueldo()));
            txtFechaIngreso.setText(empleadoAEditar.getFechaIngreso());
            txtCargo.setText(empleadoAEditar.getCargo());
        }
    }

    private void llenarComboDepartamentos() {
        comboDepartamentos.removeAllItems();
        for (Departamento dep : empresa.getDepartamentos()) {
            comboDepartamentos.addItem(dep.getNombre());
        }
    }

